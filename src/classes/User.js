class User{
    constructor(userName, password, image, firstName, lastName, email, date, city, street, streetNum){
      this.userName = userName;
      this.password = password;
      this.image = image;
      this.firstName = firstName;
      this.lastName = lastName;
      this.email = email;
      this.date = date;
      this.city = city;
      this.street = street;
      this.streetNum = streetNum;
    }
}

export default User;